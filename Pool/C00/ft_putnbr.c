#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	a;

	if (nb < 0)
	{
		a = nb;
		a = -a;
		ft_putchar('-');
	}
	else
	{
		a = nb;
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
	}
	ft_putchar(a % 10 + '0');
}
